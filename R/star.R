#' 2D Star Jump Process
#'
#' Dimensions: 2. A simple star jump process
#'
#' @param x Inputs of dimension (at least) 2.
#' @param scale01 Ignored.
#' @param mu mean of each process.
#' @param sigma standard deviation of each process.
#' @param ell lengthscale of RBF kernels for each process.
#' @return Scalar output
#' @details Uses a fixed mask in the shape of a star to determine which of two smooth processes will be returned at each location. See reference for details, but note that this implementation uses a determinstic error structure based on a linear combination of 5 RBF kernels for each process.
#' @references
#' Flowers, A. R., Franck, C. T., Binois, M., Park, C., & Gramacy, R. B. (2025). Modular Jump Gaussian Processes. arXiv preprint arXiv:2505.15557.
#'
#' @export
#' @examples
#' n <- 2
#' x <- matrix(runif(50), nrow=n)
#' y <- apply(x, 1, star2)
star2 <- function(x, scale01=TRUE, mu=c(1, -1), sigma=c(3, 3), ell=c(0.1, 0.2)){
  star101 <- get_star_matrix_private()
  mask_ind <- 1 + (round(x[1:2] * 100) %% 101)
  mask_val <- star101[mask_ind[1], mask_ind[2]]

  # Fixed RBF "landmarks" for smoothness
  centers <- matrix(
    c(0.2, 0.2,
      0.8, 0.2,
      0.5, 0.5,
      0.2, 0.8,
      0.8, 0.8), byrow=TRUE, ncol=2)
  # Fixed, deterministic weights
  weights0 <- c(0.8, -0.5, 0.3, 0.6, -0.7)
  weights1 <- c(-0.4, 0.7, -0.6, 0.2, 0.5)

  # Squared distances to each center
  dists2 <- colSums((t(centers) - x[1:2])^2)
  # RBF kernel function
  rbf <- function(d2, l=0.1) exp(-d2/(2*l^2))

  noise0 <- sum(weights0 * rbf(dists2, l=ell[1]))
  noise1 <- sum(weights1 * rbf(dists2, l=ell[2]))

  res <- (mu[1] + sigma[1] * noise0) * (1 - mask_val)
  res <- res + (mu[2] + sigma[2] * noise1) * mask_val
  return(res)
}


